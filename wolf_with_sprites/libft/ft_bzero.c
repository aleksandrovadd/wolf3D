#include "libft.h"

void	*ft_bzero(void *s, size_t n)
{
	unsigned char	*ns;
	size_t			i;

	i = 0;
	ns = (unsigned char *)s;
	while (n > 0)
	{
		ns[i] = '\0';
		n--;
		i++;
	}
	return ((void *)s);
}
