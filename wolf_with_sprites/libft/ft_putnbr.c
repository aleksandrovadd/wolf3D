#include "libft.h"

void	ft_putnbr(int nb)
{
	int	num;

	num = nb;
	if (num == (-2147483648))
		nb = (-214748364);
	if (nb < 0)
	{
		ft_putchar('-');
		nb *= -1;
	}
	if (nb >= 10)
	{
		ft_putnbr(nb / 10);
		ft_putnbr(nb % 10);
	}
	if (nb < 10)
		ft_putchar(nb + '0');
	if (num == (-2147483648))
		ft_putchar('8');
}
