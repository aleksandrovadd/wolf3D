#include "libft.h"

int	ft_atoi(const char *str)
{
	int i;
	int z;
	int nb;

	i = 0;
	z = 1;
	nb = 0;
	while (str[i] == ' ' || str[i] == '\t' || str[i] == '\v'
		|| str[i] == '\f' || str[i] == '\r' || str[i] == '\n')
		i++;
	if (str[i] == '+' && str[i + 1] >= '0' && str[i] <= '9')
		i++;
	if (str[i] == '-' && str[i + 1] >= '0' && str[i] <= '9')
	{
		z = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		nb = nb * 10 + (str[i] - 48);
		i++;
	}
	return (nb * z);
}
