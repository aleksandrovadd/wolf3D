#include "libft.h"

void	ft_putnbr_fd(int nb, int fd)
{
	int b;

	b = nb;
	if (b == (-2147483648))
		nb = (-214748364);
	if (nb < 0)
	{
		ft_putchar_fd('-', fd);
		nb *= -1;
	}
	if (nb >= 10)
	{
		ft_putnbr_fd(nb / 10, fd);
		ft_putnbr_fd(nb % 10, fd);
	}
	if (nb < 10)
		ft_putchar_fd(nb + '0', fd);
	if (b == (-2147483648))
		ft_putchar_fd('8', fd);
}
